    public void clicCase(int ligne, int colonne) {
        case_jeu c = damier.getCase(ligne, colonne);

        if (!c.estAllume()) {
            return;
        }

        cavalier.deplacer(ligne, colonne);
        c.eteindre();
        mettreAJour();
    }

    public void mettreAJour() {
        for (int i = 0; i < 6; i++) {
            for (int j = 0; j < 6; j++) {
                case_jeu c = damier.getCase(i, j);

                if (c.estAllume()) {
                    boutons[i][j].setBackground(Color.PINK);
                } else {
                    boutons[i][j].setBackground(Color.GRAY);
                }
                if (cavalier.ligne == i && cavalier.colonne == j) {
                    boutons[i][j].setText("â™Ÿ");
                } else {
                    boutons[i][j].setText("");
                }

            }
        }

    }

